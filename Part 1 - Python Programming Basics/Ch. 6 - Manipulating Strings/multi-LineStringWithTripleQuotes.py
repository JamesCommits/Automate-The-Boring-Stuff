# While you can use the \n escape character to put a newline into  a string, it is often easier to
# use multiline strings.  A multiline string in Python begins and ends with either three single quotes
# or three double quotes. Any quotes, tabs, or newlines in between the "triple quotes" are considered
# part of the string.

print('''Dear Alice,

Eve's cat has been arrested for catnapping, cat burglary, and extortion.

Sincerely,
Bob''')

# Notice that the single quote in "Eve's" did not need to be escaped.

